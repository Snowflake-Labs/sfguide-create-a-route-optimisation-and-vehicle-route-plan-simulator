-- ============================================================
-- LOAD FLEET INTELLIGENCE DATASET FROM s3://fleet-intelligence
-- ============================================================

-- 1. Create database and schema
CREATE DATABASE IF NOT EXISTS SYNTHETIC_DATASETS;
CREATE SCHEMA IF NOT EXISTS SYNTHETIC_DATASETS.FLEET_INTELLIGENCE;
USE SCHEMA SYNTHETIC_DATASETS.FLEET_INTELLIGENCE;

-- 2. Create file format for parquet
CREATE OR REPLACE FILE FORMAT parquet_ff
  TYPE = PARQUET
  REPLACE_INVALID_CHARACTERS = TRUE
  BINARY_AS_TEXT = FALSE;

-- 3. Create external stage (public bucket, no credentials needed)
CREATE OR REPLACE STAGE fleet_intelligence_s3
  URL = 's3://fleet-intelligence/'
  FILE_FORMAT = parquet_ff;


-- ============================================================
-- 4. Create tables and load data
-- ============================================================

-- GERMANY_DESTINATIONS (75,242 rows)
CREATE OR REPLACE TABLE GERMANY_DESTINATIONS (
  ID TEXT,
  NAME TEXT,
  BASIC_CATEGORY TEXT,
  LOCATION_TYPE TEXT,
  LNG FLOAT,
  LAT FLOAT,
  GEOMETRY GEOGRAPHY,
  ADDRESS TEXT,
  CITY TEXT
);

COPY INTO GERMANY_DESTINATIONS (ID, NAME, BASIC_CATEGORY, LOCATION_TYPE, LNG, LAT, GEOMETRY, ADDRESS, CITY)
FROM (
  SELECT $1:ID::TEXT, $1:NAME::TEXT, $1:BASIC_CATEGORY::TEXT, $1:LOCATION_TYPE::TEXT,
         $1:LNG::FLOAT, $1:LAT::FLOAT, TO_GEOGRAPHY($1:GEOMETRY::TEXT),
         $1:ADDRESS::TEXT, $1:CITY::TEXT
  FROM @fleet_intelligence_s3/germany_destinations/
)
FILE_FORMAT = parquet_ff;

-- GERMANY_REST_STOPS (6,315 rows)
CREATE OR REPLACE TABLE GERMANY_REST_STOPS (
  REST_STOP_ID TEXT,
  NAME TEXT,
  REST_TYPE TEXT,
  GEOMETRY GEOGRAPHY,
  CENTER_POINT GEOGRAPHY,
  LAT FLOAT,
  LNG FLOAT,
  HAS_EV_CHARGING BOOLEAN,
  AREA_M2 FLOAT,
  CAPACITY_RATING TEXT
);

COPY INTO GERMANY_REST_STOPS (REST_STOP_ID, NAME, REST_TYPE, GEOMETRY, CENTER_POINT, LAT, LNG, HAS_EV_CHARGING, AREA_M2, CAPACITY_RATING)
FROM (
  SELECT $1:REST_STOP_ID::TEXT, $1:NAME::TEXT, $1:REST_TYPE::TEXT,
         TO_GEOGRAPHY($1:GEOMETRY), TO_GEOGRAPHY($1:CENTER_POINT),
         $1:LAT::FLOAT, $1:LNG::FLOAT, $1:HAS_EV_CHARGING::BOOLEAN,
         $1:AREA_M2::FLOAT, $1:CAPACITY_RATING::TEXT
  FROM @fleet_intelligence_s3/GERMANY_REST_STOPS/
)
FILE_FORMAT = parquet_ff;

-- TRUCK_FLEET (500 rows)
CREATE OR REPLACE TABLE TRUCK_FLEET (
  TRUCK_ID TEXT,
  HOME_BASE_ID TEXT,
  HOME_BASE_NAME TEXT,
  HOME_LNG FLOAT,
  HOME_LAT FLOAT,
  HOME_GEOMETRY GEOGRAPHY,
  HOME_CITY TEXT,
  TRUCK_TYPE TEXT,
  DRIVER_PROFILE TEXT,
  BASE_SPEED_KMH FLOAT,
  SHIFT_TYPE TEXT
);

COPY INTO TRUCK_FLEET (TRUCK_ID, HOME_BASE_ID, HOME_BASE_NAME, HOME_LNG, HOME_LAT, HOME_GEOMETRY, HOME_CITY, TRUCK_TYPE, DRIVER_PROFILE, BASE_SPEED_KMH, SHIFT_TYPE)
FROM (
  SELECT $1:TRUCK_ID::TEXT, $1:HOME_BASE_ID::TEXT, $1:HOME_BASE_NAME::TEXT,
         $1:HOME_LNG::FLOAT, $1:HOME_LAT::FLOAT, TO_GEOGRAPHY($1:HOME_GEOMETRY::TEXT),
         $1:HOME_CITY::TEXT, $1:TRUCK_TYPE::TEXT, $1:DRIVER_PROFILE::TEXT,
         $1:BASE_SPEED_KMH::FLOAT, $1:SHIFT_TYPE::TEXT
  FROM @fleet_intelligence_s3/truck_fleet/
)
FILE_FORMAT = parquet_ff;

-- TRIP_SCHEDULE (9,343 rows)
CREATE OR REPLACE TABLE TRIP_SCHEDULE (
  TRUCK_ID TEXT,
  DRIVER_PROFILE TEXT,
  TRIP_DATE DATE,
  TRIP_TYPE TEXT,
  ROUTE_VARIATION TEXT,
  ORIGIN_ID TEXT,
  DEST_ID TEXT,
  SHIFT_START_TIME TEXT,
  ROUTE_DEVIATION_FACTOR FLOAT,
  TRIP_SEQ NUMBER
);

COPY INTO TRIP_SCHEDULE (TRUCK_ID, DRIVER_PROFILE, TRIP_DATE, TRIP_TYPE, ROUTE_VARIATION, ORIGIN_ID, DEST_ID, SHIFT_START_TIME, ROUTE_DEVIATION_FACTOR, TRIP_SEQ)
FROM (
  SELECT $1:TRUCK_ID::TEXT, $1:DRIVER_PROFILE::TEXT, $1:TRIP_DATE::DATE,
         $1:TRIP_TYPE::TEXT, $1:ROUTE_VARIATION::TEXT, $1:ORIGIN_ID::TEXT,
         $1:DEST_ID::TEXT, $1:SHIFT_START_TIME::TEXT, $1:ROUTE_DEVIATION_FACTOR::FLOAT,
         $1:TRIP_SEQ::NUMBER
  FROM @fleet_intelligence_s3/trip_schedule/
)
FILE_FORMAT = parquet_ff;

-- FACT_TRUCK_TELEMETRY (15,214,038 rows - 137 parquet parts)
CREATE OR REPLACE TABLE FACT_TRUCK_TELEMETRY (
  TELEMETRY_ID TEXT,
  TRUCK_ID TEXT,
  DRIVER_ID TEXT,
  TRIP_ID TEXT,
  TS TIMESTAMP_NTZ,
  LATITUDE FLOAT,
  LONGITUDE FLOAT,
  SPEED_KMH FLOAT,
  HEADING_DEG FLOAT,
  POSTED_SPEED_KMH FLOAT,
  STATUS TEXT,
  IS_SPEEDING BOOLEAN,
  IS_HOS_VIOLATION BOOLEAN,
  IS_DETOUR BOOLEAN,
  GPS_ACCURACY_M FLOAT,
  LOCATION_ID TEXT,
  LOCATION_TYPE TEXT,
  CREATED_AT TIMESTAMP_NTZ,
  GEOMETRY GEOGRAPHY
);

COPY INTO FACT_TRUCK_TELEMETRY (TELEMETRY_ID, TRUCK_ID, DRIVER_ID, TRIP_ID, TS, LATITUDE, LONGITUDE, SPEED_KMH, HEADING_DEG, POSTED_SPEED_KMH, STATUS, IS_SPEEDING, IS_HOS_VIOLATION, IS_DETOUR, GPS_ACCURACY_M, LOCATION_ID, LOCATION_TYPE, CREATED_AT, GEOMETRY)
FROM (
  SELECT $1:TELEMETRY_ID::TEXT, $1:TRUCK_ID::TEXT, $1:DRIVER_ID::TEXT, $1:TRIP_ID::TEXT,
         $1:TS::VARCHAR::TIMESTAMP_NTZ,
         $1:LATITUDE::FLOAT, $1:LONGITUDE::FLOAT,
         $1:SPEED_KMH::FLOAT, $1:HEADING_DEG::FLOAT, $1:POSTED_SPEED_KMH::FLOAT,
         $1:STATUS::TEXT, $1:IS_SPEEDING::BOOLEAN, $1:IS_HOS_VIOLATION::BOOLEAN,
         $1:IS_DETOUR::BOOLEAN, $1:GPS_ACCURACY_M::FLOAT, $1:LOCATION_ID::TEXT,
         $1:LOCATION_TYPE::TEXT, $1:CREATED_AT::VARCHAR::TIMESTAMP_NTZ,
         ST_GEOGRAPHYFROMWKT($1:GEOMETRY_WKT::TEXT)
  FROM @fleet_intelligence_s3/fact_truck_telemetry/
)
FILE_FORMAT = parquet_ff;


-- ============================================================
-- 5. Verify loaded data
-- ============================================================
SELECT 'GERMANY_DESTINATIONS' AS TBL, COUNT(*) AS NUM_ROWS FROM GERMANY_DESTINATIONS
UNION ALL SELECT 'GERMANY_REST_STOPS', COUNT(*) FROM GERMANY_REST_STOPS
UNION ALL SELECT 'TRUCK_FLEET', COUNT(*) FROM TRUCK_FLEET
UNION ALL SELECT 'TRIP_SCHEDULE', COUNT(*) FROM TRIP_SCHEDULE
UNION ALL SELECT 'FACT_TRUCK_TELEMETRY', COUNT(*) FROM FACT_TRUCK_TELEMETRY
ORDER BY TBL;
