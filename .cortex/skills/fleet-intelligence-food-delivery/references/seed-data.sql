    LOCATION       GEOGRAPHY,
    NAME           VARCHAR,
    CUISINE_TYPE   VARCHAR,
    ADDRESS        VARCHAR,
    CITY           VARCHAR,
    STATE          VARCHAR
);

COPY INTO FLEET_INTELLIGENCE.FLEET_INTELLIGENCE_FOOD_DELIVERY.RESTAURANTS
FROM @FLEET_INTELLIGENCE.FLEET_INTELLIGENCE_FOOD_DELIVERY.SEED_STAGE/RESTAURANTS/
