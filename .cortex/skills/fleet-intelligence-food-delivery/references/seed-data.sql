--------------------------------------------------------------------
-- VALIDATION
--------------------------------------------------------------------
SELECT 'RESTAURANTS' AS TBL, COUNT(*) AS ROWS FROM FLEET_INTELLIGENCE.FLEET_INTELLIGENCE_FOOD_DELIVERY.RESTAURANTS
UNION ALL SELECT 'CUSTOMER_ADDRESSES', COUNT(*) FROM FLEET_INTELLIGENCE.FLEET_INTELLIGENCE_FOOD_DELIVERY.CUSTOMER_ADDRESSES
UNION ALL SELECT 'COURIERS', COUNT(*) FROM FLEET_INTELLIGENCE.FLEET_INTELLIGENCE_FOOD_DELIVERY.COURIERS
UNION ALL SELECT 'DELIVERY_ROUTE_GEOMETRIES', COUNT(*) FROM FLEET_INTELLIGENCE.FLEET_INTELLIGENCE_FOOD_DELIVERY.DELIVERY_ROUTE_GEOMETRIES
UNION ALL SELECT 'COURIER_LOCATIONS', COUNT(*) FROM FLEET_INTELLIGENCE.FLEET_INTELLIGENCE_FOOD_DELIVERY.COURIER_LOCATIONS;
