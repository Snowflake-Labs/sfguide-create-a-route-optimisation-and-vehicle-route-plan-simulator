    ORIGIN_LNG                 FLOAT,
    ORIGIN_LAT                 FLOAT,
    ORIGIN_NAME                VARCHAR,
    ORIGIN_CITY                VARCHAR,
    DEST_LNG                   FLOAT,
    DEST_LAT                   FLOAT,
    DEST_NAME                  VARCHAR,
    DEST_CITY                  VARCHAR,
    STRAIGHT_LINE_DISTANCE_KM  FLOAT,
    EXPECTED_DISTANCE_KM       FLOAT,
    EXPECTED_DURATION_SEC      FLOAT,
    EXPECTED_DURATION_MIN      FLOAT,
    EXPECTED_PATH              GEOGRAPHY
