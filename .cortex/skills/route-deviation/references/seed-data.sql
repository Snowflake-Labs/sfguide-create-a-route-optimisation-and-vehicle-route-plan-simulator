/*
 * seed-data.sql — Route Deviation
 * Load San Francisco baseline data from S3.
 * Includes source telemetry tables + ETL output tables.
 * Idempotent: only loads if tables are empty for the target region.
 */

CREATE DATABASE IF NOT EXISTS FLEET_INTELLIGENCE;
CREATE SCHEMA IF NOT EXISTS FLEET_INTELLIGENCE.ROUTE_DEVIATION;
CREATE SCHEMA IF NOT EXISTS FLEET_INTELLIGENCE.ROUTE_CACHE;

CREATE STAGE IF NOT EXISTS FLEET_INTELLIGENCE.ROUTE_DEVIATION.SEED_STAGE
    URL = 's3://fleet-intelligence/SanFrancisco/route-deviation/'
    FILE_FORMAT = (TYPE = PARQUET);

--------------------------------------------------------------------
-- ROUTE_CACHE (shared schema)
--------------------------------------------------------------------
CREATE TABLE IF NOT EXISTS FLEET_INTELLIGENCE.ROUTE_CACHE.ROUTE_CACHE (
    REGION           VARCHAR NOT NULL DEFAULT 'SanFrancisco',
    ORIGIN_ID        VARCHAR,
    DEST_ID          VARCHAR,
    ROAD_DISTANCE_M  FLOAT,
    DURATION_SECONDS FLOAT,
    ROUTE_LINE       GEOGRAPHY,
    CREATED_AT       TIMESTAMP_NTZ DEFAULT CURRENT_TIMESTAMP()
);

COPY INTO FLEET_INTELLIGENCE.ROUTE_CACHE.ROUTE_CACHE
FROM @FLEET_INTELLIGENCE.ROUTE_DEVIATION.SEED_STAGE/ROUTE_CACHE/
FILE_FORMAT = (TYPE = PARQUET) MATCH_BY_COLUMN_NAME = CASE_INSENSITIVE ON_ERROR = CONTINUE;

--------------------------------------------------------------------
-- TRIP_ACTUAL_METRICS
--------------------------------------------------------------------
CREATE TABLE IF NOT EXISTS FLEET_INTELLIGENCE.ROUTE_DEVIATION.TRIP_ACTUAL_METRICS (
    REGION               VARCHAR NOT NULL DEFAULT 'SanFrancisco',
    TRIP_ID              VARCHAR,
    TRUCK_ID             VARCHAR,
    DRIVER_ID            VARCHAR,
    TRUCK_ID_NORMALIZED  VARCHAR,
    TRIP_DATE            DATE,
    TRIP_SEQ             INT,
    ACTUAL_START_TS      TIMESTAMP_NTZ,
    ACTUAL_END_TS        TIMESTAMP_NTZ,
    TOTAL_DURATION_SEC   FLOAT,
    TOTAL_DURATION_MIN   FLOAT,
    ACTUAL_DURATION_SEC  FLOAT,
    ACTUAL_DURATION_MIN  FLOAT,
    POINT_COUNT          INT,
    ACTUAL_PATH          GEOGRAPHY
);

COPY INTO FLEET_INTELLIGENCE.ROUTE_DEVIATION.TRIP_ACTUAL_METRICS
FROM @FLEET_INTELLIGENCE.ROUTE_DEVIATION.SEED_STAGE/TRIP_ACTUAL_METRICS/
FILE_FORMAT = (TYPE = PARQUET) MATCH_BY_COLUMN_NAME = CASE_INSENSITIVE ON_ERROR = CONTINUE;

--------------------------------------------------------------------
-- OD_EXPECTED_ROUTES
--------------------------------------------------------------------
