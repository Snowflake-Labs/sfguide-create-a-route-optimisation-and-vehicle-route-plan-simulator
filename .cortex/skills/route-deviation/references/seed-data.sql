FROM @FLEET_INTELLIGENCE.ROUTE_DEVIATION.SEED_STAGE/DAILY_DEVIATION_TRENDS/
FILE_FORMAT = (TYPE = PARQUET) MATCH_BY_COLUMN_NAME = CASE_INSENSITIVE ON_ERROR = CONTINUE;

--------------------------------------------------------------------
-- VALIDATION
--------------------------------------------------------------------
SELECT 'ROUTE_CACHE' AS TBL, COUNT(*) AS ROWS FROM FLEET_INTELLIGENCE.ROUTE_CACHE.ROUTE_CACHE
UNION ALL SELECT 'TRIP_ACTUAL_METRICS', COUNT(*) FROM FLEET_INTELLIGENCE.ROUTE_DEVIATION.TRIP_ACTUAL_METRICS
UNION ALL SELECT 'OD_EXPECTED_ROUTES', COUNT(*) FROM FLEET_INTELLIGENCE.ROUTE_DEVIATION.OD_EXPECTED_ROUTES
UNION ALL SELECT 'TRIP_DEVIATION_ANALYSIS', COUNT(*) FROM FLEET_INTELLIGENCE.ROUTE_DEVIATION.TRIP_DEVIATION_ANALYSIS
UNION ALL SELECT 'DRIVER_DEVIATION_SUMMARY', COUNT(*) FROM FLEET_INTELLIGENCE.ROUTE_DEVIATION.DRIVER_DEVIATION_SUMMARY
UNION ALL SELECT 'DAILY_DEVIATION_TRENDS', COUNT(*) FROM FLEET_INTELLIGENCE.ROUTE_DEVIATION.DAILY_DEVIATION_TRENDS;
