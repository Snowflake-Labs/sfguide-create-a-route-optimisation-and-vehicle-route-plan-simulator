COPY INTO FLEET_INTELLIGENCE.ROUTE_DEVIATION.OD_EXPECTED_ROUTES
FROM @FLEET_INTELLIGENCE.ROUTE_DEVIATION.SEED_STAGE/OD_EXPECTED_ROUTES/
FILE_FORMAT = (TYPE = PARQUET) MATCH_BY_COLUMN_NAME = CASE_INSENSITIVE ON_ERROR = CONTINUE;

--------------------------------------------------------------------
-- TRIP_DEVIATION_ANALYSIS
--------------------------------------------------------------------
