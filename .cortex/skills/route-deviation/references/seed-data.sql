    REGION                    VARCHAR NOT NULL DEFAULT 'SanFrancisco',
    TRIP_DATE                 DATE,
    DAY_OF_WEEK               VARCHAR,
    TOTAL_TRIPS               INT,
    DEVIATION_TRIPS           INT,
